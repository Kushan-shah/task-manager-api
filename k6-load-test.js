import http from 'k6/http';
import { check, sleep, group } from 'k6';
import { Rate, Trend, Counter } from 'k6/metrics';

// ========================
// CUSTOM METRICS
// ========================
const errorRate = new Rate('error_rate');
const registerDuration = new Trend('register_duration', true);
const loginDuration = new Trend('login_duration', true);
const createTaskDuration = new Trend('create_task_duration', true);
const getTasksDuration = new Trend('get_tasks_duration', true);
const dashboardDuration = new Trend('dashboard_duration', true);
const aiInsightDuration = new Trend('ai_insight_duration', true);
const tasksCreated = new Counter('tasks_created');

// ========================
// TEST CONFIG
// ========================
const BASE_URL = 'http://localhost:8080';

export const options = {
  stages: [
    { duration: '10s', target: 10 },   // Ramp up to 10 users
    { duration: '20s', target: 25 },   // Ramp up to 25 users
    { duration: '30s', target: 50 },   // Peak: 50 concurrent users
    { duration: '10s', target: 0 },    // Ramp down
  ],
  thresholds: {
    http_req_duration: ['p(95)<2000'],  // 95th percentile < 2s
    error_rate: ['rate<0.10'],          // Error rate < 10%
  },
};

// ========================
// SETUP: Register a shared test user
// ========================
export function setup() {
  const uniqueId = Date.now();
  const payload = JSON.stringify({
    name: `K6 Load Tester ${uniqueId}`,
    email: `k6_load_${uniqueId}@test.com`,
    password: 'TestPassword123!',
  });

  const headers = { 'Content-Type': 'application/json' };

  // Register
  const regRes = http.post(`${BASE_URL}/api/auth/register`, payload, { headers });
  check(regRes, { 'setup: register succeeded': (r) => r.status === 200 || r.status === 201 });

  // Login
  const loginPayload = JSON.stringify({
    email: `k6_load_${uniqueId}@test.com`,
    password: 'TestPassword123!',
  });

  const loginRes = http.post(`${BASE_URL}/api/auth/login`, loginPayload, { headers });
  check(loginRes, { 'setup: login succeeded': (r) => r.status === 200 });

  const token = loginRes.json('token');
  if (!token) {
    console.error('SETUP FAILED: Could not obtain JWT token.');
    return {};
  }

  return { token, email: `k6_load_${uniqueId}@test.com` };
}

// ========================
// MAIN VU SCENARIO
// ========================
export default function (data) {
  if (!data.token) {
    console.error('No token available. Aborting VU.');
    return;
  }

  const headers = {
    'Content-Type': 'application/json',
    'Authorization': `Bearer ${data.token}`,
  };

  let taskId = null;

  // ---------- 1. CREATE TASK ----------
  group('Create Task', () => {
    const taskPayload = JSON.stringify({
      title: `K6 Task ${__VU}-${__ITER}`,
      description: `Automated load test task generated by VU ${__VU}, iteration ${__ITER}. This task is designed to stress-test the API under concurrent load conditions.`,
      priority: ['LOW', 'MEDIUM', 'HIGH'][Math.floor(Math.random() * 3)],
      dueDate: '2026-12-31',
    });

    const res = http.post(`${BASE_URL}/api/tasks`, taskPayload, { headers });
    createTaskDuration.add(res.timings.duration);

    const success = check(res, {
      'create task: status 201': (r) => r.status === 201,
    });
    errorRate.add(!success);

    if (success && res.json('id')) {
      taskId = res.json('id');
      tasksCreated.add(1);
    }
  });

  sleep(0.3);

  // ---------- 2. LIST TASKS (PAGINATED) ----------
  group('List Tasks', () => {
    const res = http.get(`${BASE_URL}/api/tasks?page=0&size=10&sortBy=createdAt&sortDir=desc`, { headers });
    getTasksDuration.add(res.timings.duration);

    const success = check(res, {
      'list tasks: status 200': (r) => r.status === 200,
    });
    errorRate.add(!success);
  });

  sleep(0.3);

  // ---------- 3. DASHBOARD (CACHED) ----------
  group('Dashboard', () => {
    const res = http.get(`${BASE_URL}/api/tasks/dashboard`, { headers });
    dashboardDuration.add(res.timings.duration);

    const success = check(res, {
      'dashboard: status 200': (r) => r.status === 200,
    });
    errorRate.add(!success);
  });

  sleep(0.3);

  // ---------- 4. GET TASK BY ID ----------
  if (taskId) {
    group('Get Task By ID', () => {
      const res = http.get(`${BASE_URL}/api/tasks/${taskId}`, { headers });

      const success = check(res, {
        'get task: status 200': (r) => r.status === 200,
      });
      errorRate.add(!success);
    });

    sleep(0.2);

    // ---------- 5. AI INSIGHTS ----------
    group('AI Insights', () => {
      const res = http.get(`${BASE_URL}/api/tasks/${taskId}/ai`, { headers });
      aiInsightDuration.add(res.timings.duration);

      const success = check(res, {
        'ai insights: status 200': (r) => r.status === 200,
      });
      errorRate.add(!success);
    });

    sleep(0.2);

    // ---------- 6. UPDATE TASK ----------
    group('Update Task', () => {
      const updatePayload = JSON.stringify({
        title: `K6 Updated ${__VU}-${__ITER}`,
        description: `Updated by k6 VU ${__VU}`,
        status: 'IN_PROGRESS',
        priority: 'HIGH',
        dueDate: '2026-12-31',
      });

      const res = http.put(`${BASE_URL}/api/tasks/${taskId}`, updatePayload, { headers });

      const success = check(res, {
        'update task: status 200': (r) => r.status === 200,
      });
      errorRate.add(!success);
    });

    sleep(0.2);

    // ---------- 7. SEARCH ----------
    group('Search Tasks', () => {
      const res = http.get(`${BASE_URL}/api/tasks/search?keyword=K6`, { headers });

      const success = check(res, {
        'search: status 200': (r) => r.status === 200,
      });
      errorRate.add(!success);
    });

    sleep(0.2);

    // ---------- 8. DELETE TASK ----------
    group('Delete Task', () => {
      const res = http.del(`${BASE_URL}/api/tasks/${taskId}`, null, { headers });

      const success = check(res, {
        'delete task: status 204 or 200': (r) => r.status === 204 || r.status === 200,
      });
      errorRate.add(!success);
    });
  }

  sleep(0.5);
}
